CREATE COLUMN TABLE BENCHMARKS.queries (
    Q_Key				INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    Q_Group				INTEGER NOT NULL,
    Q_Query				INTEGER NOT NULL,
    PRIMARY	KEY(Q_Key)
);

CREATE COLUMN TABLE BENCHMARKS.indices (
    I_ConfigKey 		INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    I_ConfigName 		VARCHAR(20) NOT NULL,
    PRIMARY KEY(I_Configkey)
);

CREATE COLUMN TABLE BENCHMARKS.hints (
    I_HintKey 		INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    I_HintName 		VARCHAR(20) NOT NULL,
    PRIMARY KEY(I_HintKey)
);

CREATE COLUMN TABLE BENCHMARKS.Measurements (
    M_Key				INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
--dims
    M_QueryKey 			INTEGER NOT NULL,
    M_IndexConfigKey	INTEGER NOT NULL,
    M_HintKey			INTEGER NOT NULL,
	M_Column			BOOLEAN NOT NULL,
    M_CpuCount			INTEGER NOT NULL,
    M_ThreadCount		INTEGER NOT NULL,
    M_ScaleFactor		INTEGER NOT NULL,
    M_Repetition 		INTEGER NOT NULL,
    
--measures
    M_RunTime 			INTEGER,
    M_CursTime 			INTEGER,
    PRIMARY KEY(M_Key)   
);
